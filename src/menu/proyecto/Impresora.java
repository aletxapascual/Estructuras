    private void insertarImagen(JLabel label, String ruta) {
        try {
        // Cargar la imagen desde el classpath
            InputStream is = getClass().getResourceAsStream(ruta);
            if (is != null) {
                imagen = new ImageIcon(ImageIO.read(is));
                icono = new ImageIcon(imagen.getImage().getScaledInstance(
                    label.getWidth(), label.getHeight(), Image.SCALE_SMOOTH));
                label.setIcon(icono);
            } else {
                System.err.println("No se encontró la imagen en la ruta: " + ruta);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    
