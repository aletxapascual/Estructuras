    private String imprimirValor() {
        if (!cola.isEmpty()) {
            return String.valueOf(cola.poll()); // Convierte el elemento eliminado en String y lo retorna
        } else {
            return "La cola está vacía."; // Mensaje en caso de que la cola esté vacía
        }
    }
    
    private void imprimirCola(){
        cola1.setText(leerPosicion(0));
        cola2.setText(leerPosicion(1));
        cola3.setText(leerPosicion(2));
        cola4.setText(leerPosicion(3));
        cola5.setText(leerPosicion(4));
        cola6.setText(leerPosicion(5));
    }

    private String leerPosicion(int posicion) {
        int indice = 0;
        for (Object elemento : cola) { // Itera directamente sobre los elementos de la cola
            if (indice == posicion) {
                return String.valueOf(elemento); // Retorna el elemento convertido a String
            }
            indice++;
        }

    return null; // Esto nunca se alcanza, pero se incluye por estructura
    }
